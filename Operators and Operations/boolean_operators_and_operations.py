import random

on = True
off = False

# and boolean operator
print("The and boolean operator")
print("T and T: ", on and on)
print("T and F: ", on and off)
print("F and T: ",off and on)
print("F and F: ", off and off)
print("\n")

# not boolean operator
print("The not boolean operator")
print("NOT on: ", not on)
print("NOT off: ", not off)


